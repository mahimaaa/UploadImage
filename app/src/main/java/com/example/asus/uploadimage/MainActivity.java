public class MainActivity extends AppCompatActivity {
    Button b;
    Button butt;
    ImageView i;
    private StorageReference mStorage;
    private static final int GALLERY_INTENT=2;
    private Uri downloadUrl;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        b = (Button) findViewById(R.id.upload);
       // butt=(Button)findViewById(R.id.button1);
        //i=(ImageView)findViewById(R.id.im);
        mStorage = FirebaseStorage.getInstance().getReference();

        b.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent i = new Intent(Intent.ACTION_PICK);
                i.setType("image/*");
                startActivityForResult(i, GALLERY_INTENT);


            }
        });;

    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if(requestCode==GALLERY_INTENT&&resultCode==RESULT_OK)
        {
            Uri file = data.getData();
            StorageReference riversRef = mStorage.child("Photo").child("bastar").child("cuisine1").child("!");

            riversRef.putFile(file)
                    .addOnSuccessListener(new OnSuccessListener<UploadTask.TaskSnapshot>() {
                        @Override
                        public void onSuccess(UploadTask.TaskSnapshot taskSnapshot) {
                            // Get a URL to the uploaded content
                            //  Uri downloadUrl = taskSnapshot.getDownloadUrl();
                            Toast.makeText(MainActivity.this,"Daalra",Toast.LENGTH_LONG).show();
                        }
                    })
                    .addOnFailureListener(new OnFailureListener() {
                        @Override
                        public void onFailure(@NonNull Exception exception) {
                            // Handle unsuccessful uploads
                            // ...
                            Toast.makeText(MainActivity.this,"nai Daalra",Toast.LENGTH_LONG).show();
                        }
                    });
        }
    }

}
